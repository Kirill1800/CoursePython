# Если год не делится на 4 или делится на 100, но не на 400,
# то он обычный. Во всех остальных случаях - високосный.
def vis_year(year):
    print(" Год:", year)
    if (year % 4 != 0) or ((year % 100 == 0) and (year % 400 != 0)):
        return print(" True")
    else:
        return print(" False")


