def season(num_month):
    print(" Исходный номер месяца:", num_month)
    if 9 <= num_month <= 11:
        print(" Пора года: Осень")
    elif (0 < num_month <= 2) or (num_month == 12):
        print(" Пора года: Зима")
    elif 3 <= num_month <= 5:
        print(" Пора года: Весна")
    elif 6 <= num_month <= 8:
        print(" Пора года: Лето")
    else:
        print(" Некректное число")


