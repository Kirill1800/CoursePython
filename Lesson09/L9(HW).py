print("-------------------------------------------- Задание 6 (63)")

'''
6) Проверить телефонный номер (номер должен быть длиной 10 знаков и начинаться с 8 или 9)
'''

string = "9999999999 8999999999 7999999999 9999999x99"
result = re.findall(r'[8-9][0-9]{9}', string)  # Отдельные слова
print(result)

print("-------------------------------------------- Задание 7 (63)")

'''
7) Проверить телефонный номер (номер должен быть длиной 10 знаков и начинаться с 8 или 9)
'''

